<h2>Configuration example</h2>
<div>
    <h4>Plugin description</h4>
  	<p>
          Use a custom mail server to send mails in WP. This way you can also send emails when testing on your local server.
  	</p>
    <h4>Gmail settings</h4>
  	<p>
       You can use the following settings to use the Gmail servers to send your WP mails.
  	</p>
    <ul>
        <li>From email : email@mysite.com</li>
<li>From name : Your Name</li>
<li>SMTP Host : smtp.gmail.com</li>
<li>SMTP Port : 465</li>
<li>SMTP Secure : true</li>
<li>Auth : true</li>
<li>Auth username : gmail email address</li>
<li>Auth password : gmail password</li>
    </ul>
</div>

